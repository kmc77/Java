package ex05_5_for;

public class ForExample1_1 {

	public static void main(String[] args) {
		
		
		for (int cnt = 1; cnt <= 10; cnt++) {
			System.out.println(cnt);
		}
		
		System.out.println("끝.==============");
		
		
		for (int cnt = 10; cnt >= 1; cnt--) {
			System.out.println(cnt);
		}
		
		System.out.println("끝.==============");

	}

}
