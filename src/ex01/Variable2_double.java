package ex01;

public class Variable2_double {
	public static void main(String[] atgs) {
		
		System.out.println("============실수형 변수============");
		// 실수형 변수
		// f나 F를 숫자 뒤에 붙입니다.
		float ft = 3.14f; // f가 빠진 double은 넣을 수가 없다.
		
		// d나 D를 숫자 뒤에 붙이거나 생략할 수 있습니다.
		double d = 42.195;
		
		System.out.println("f=" + ft + " d=" + d);
	}

}
